
/**
 * Additions is an abstract class that covers both Soy Milk and Sugar Syrup
 * An addition can be added to an ArrayList in the beverage object
 * which simulates adding something to a drink
 * 
 * @author Blake
 *
 */
public abstract class Additions {
    
    /**
     * Constructor for the Additions class 
     */
    public Additions() {
    }
    
    public abstract Money getCost();
    public abstract int getCalories();
        
    
        
    

}
